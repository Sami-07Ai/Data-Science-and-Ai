 # rules for naming variables
# 1. Variable names must start with a letter or underscore (_).
# 2. Variable names cannot start with a number. 
# 3. Variable names can only contain letters, numbers, and underscores.
# 4. Variable names are case-sensitive. 
# 5. Variable names cannot be a reserved keyword in Python (e.g., `if`, `else`, `while`, etc.).
# 6. Variable names should be descriptive and meaningful to improve code readability.
# 7. Avoid using special characters or spaces in variable names.
# 8. Use lowercase letters for variable names, and separate words with underscores (e.g., `my_variable`).
# 9. Keep variable names concise but clear. Avoid using long names that are difficult to understand.


